// Alias map: resolve any slug to a SPRITES key
export const SPRITE_SLUG_MAP: Record<string, string> = {
  frontend: "fe", backend: "be",
  "ai-eng": "ai", ai_eng: "ai",
  eng: "be",       // Founding Engineer → backend sprite + green
  gtm: "cmo",      // GTM Lead → cmo sprite + orange
  human: "generic",
};

export function resolveSprite(slug: string): Sprite {
  const key = SPRITE_SLUG_MAP[slug.toLowerCase()] ?? slug.toLowerCase();
  return SPRITES[key] ?? SPRITES.generic;
}
